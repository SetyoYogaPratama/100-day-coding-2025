public class day15 {
    
    public static void main(String[] args) {
        int a = 10;
        int b = 4;

        int hasilTambah = a + b; // penjumlahan
        int hasilKurang = a - b; // pengurangan

        System.out.println("Hasil " + a + " + " + b + " = " + hasilTambah);
        System.out.println("Hasil " + a + " - " + b + " = " + hasilKurang);
    
    }
    
}
